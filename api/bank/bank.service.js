const  {updateBalance, getBankAccount, getBankTransactions} = require('./bank.dao');

module.exports = {
    updateBalance: (req, res) => {
        const body = req.body;

        updateBalance(body, (err, results) => {
            if (err) {
                console.log(err);
                return res.status(500).json({
                    success: 0,
                    message: "Database connection error"
                });
            }
            return res.status(200).json({
                success: 1,
                message: "Bank balance updated successfully"
            });
        });
    },

    getBankAccount: (req, res) => { 
        getBankAccount((err, results) => {
            if (err) {
                console.log(err);
                return res.status(500).json({
                    success: 0,
                    message: "Database connection error"
                });
            }
            return res.status(200).json({
                success: 1,
                data: results
            });
        });
    },

    getBankTransactions: (req, res) => {
        getBankTransactions((err, results) => {
            if (err) {
                console.log(err);
                return res.status(500).json({
                    success: 0,
                    message: "Database connection error"
                });
            }
            return res.status(200).json({
                success: 1,
                data: results
            });
        });
    }
};